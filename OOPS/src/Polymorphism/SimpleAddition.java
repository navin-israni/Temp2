package Polymorphism;

public class SimpleAddition {
	// Method Overloading
int add (int a, int b)
{
	return (a+b);
}
int add (int a, int b, int c)
{
	return (a+b+c);
}
int add (int a, int b, int c, int d)
{
	return (a+b+c+d);
}
String add (String s1, String s2)
{
	return (s1+s2);
}
double add (double d1, double d2)
{
return (d1+d2);
}
}